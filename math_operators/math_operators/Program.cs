// Operators and order of evaluation

int num1 = 5;
int num2 = 13;

//Addition
Console.WriteLine($"Addition num1 + num2 = " +(num1 + num2));

//Subtraction
Console.WriteLine("Subtraction of num1 - num2 = " + (num1 - num2));

//Multiplication
Console.WriteLine("Multiply num1 & num2 = " + (num1 * num2));

//Division
Console.WriteLine("Divide num1 by num2 = " + (num1 / (double)(num2)));